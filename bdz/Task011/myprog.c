#include <stdio.h>
#include <errno.h>
#include <math.h>

int main() {
    errno = 0;  // обнуляем перед первым вызовом

    double result = sqrt(-1.0);  // ошибка: извлечение корня из отрицательного
    if (result != result) {      // проверка на NaN (указывает на ошибку)
        printf("Ошибка: %d\n", errno);  // выведет, например, EDOM (33)
    }

    // Теперь вызываем успешную функцию
    result = sqrt(25.0);  // успешно, result = 5.0
    // Функция не сбросила errno, он всё ещё равен 33

    // Если мы ошибочно подумаем, что второй вызов тоже мог установить errno,
    // и напишем:
    if (result == 5.0) {  // это не проверка ошибки, а просто условие
        printf("Успех! errno = %d\n", errno);  // выведет 33, хотя ошибки нет!
    }
    return 0;
}
